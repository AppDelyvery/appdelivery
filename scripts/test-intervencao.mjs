import { readFileSync } from "node:fs";
import pg from "pg";
const env = Object.fromEntries(readFileSync(new URL("../.env.local", import.meta.url), "utf8").split(/\r?\n/).filter((l) => l.includes("=") && !l.startsWith("#")).map((l) => { const i = l.indexOf("="); return [l.slice(0, i).trim(), l.slice(i + 1).trim()]; }));
const m = env.DATABASE_URL.match(/^postgres(?:ql)?:\/\/([^:]+):(.*)@([^:\/]+):(\d+)\/(.+)$/);
const c = new pg.Client({ user: m[1], password: m[2], host: m[3], port: +m[4], database: m[5], ssl: { rejectUnauthorized: false } });
await c.connect();
const adm = (await c.query("select id from auth.users where email='demo.admin@gmail.com'")).rows[0].id;
const ped = (await c.query("select id, entregador_id, preco_entregador, risco_motivo from pedidos where em_risco and status='coletado' limit 1")).rows[0];
const antes = (await c.query("select total_entregas, saldo from entregadores where id=$1", [ped.entregador_id])).rows[0];
await c.query("begin");
await c.query("set local role authenticated");
await c.query("select set_config('request.jwt.claims', json_build_object('sub',$1::text,'role','authenticated')::text, true)", [adm]);
const r = (await c.query("select admin_forcar_conclusao($1) as ret", [ped.id])).rows[0].ret;
const pd = (await c.query("select status, em_risco from pedidos where id=$1", [ped.id])).rows[0];
const dep = (await c.query("select total_entregas, saldo from entregadores where id=$1", [ped.entregador_id])).rows[0];
await c.query("rollback");
console.log("pedido em risco:", ped.risco_motivo);
console.log("RPC retornou:", r);
console.log("status:", pd.status, "| em_risco:", pd.em_risco);
console.log("entregador total_entregas:", antes.total_entregas, "->", dep.total_entregas, "| saldo:", antes.saldo, "->", dep.saldo, "(+", ped.preco_entregador, ")");
await c.end();
